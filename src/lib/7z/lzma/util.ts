/**
 * Create probability prices lookup table
 * @return `length === 0x200`
 */
export function createProbPrices(): CProbPrice[] {
  const probPrices: CProbPrice[] = [];
  for (let i = 9; i--;) {
    const start = 1 << (9 - i - 1);
    const end = 1 << (9 - i);

    for (let j = start; j < end; ++j) {
      probPrices[j] = (i << 6) + ((end - j) << 6 >>> (9 - i - 1));
    }
  }

  return probPrices;
}
