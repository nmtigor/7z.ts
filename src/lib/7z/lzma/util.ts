/**
 * Get position slot for a distance value
 * @param @param pos_x
 */
export function getPosSlot(pos_x: uint32): uint8 {
  if (pos_x < 0x800) return G_FAST_POS[pos_x];
  if (pos_x < 0x20_0000) return G_FAST_POS[pos_x >> 10] + 20;
  return G_FAST_POS[pos_x >> 20] + 40;
}
